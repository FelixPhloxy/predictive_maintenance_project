import reveal_slides as rs
import streamlit as st


def presentation_page():
    st.title("Презентация проекта")

    presentation_markdown = """
# Прогнозирование отказов оборудования

---

## Введение
- Задача: бинарная классификация состояния промышленного оборудования.
- Цель: предсказать отказ оборудования (`Machine failure = 1`) или его отсутствие (`Machine failure = 0`).
- Формат результата: многостраничное Streamlit-приложение с отдельной страницей презентации.

---

## Датасет
- Используется AI4I 2020 Predictive Maintenance Dataset.
- Объем данных: 10 000 записей.
- Источник: UCI Machine Learning Repository.
- Целевая переменная: `Machine failure`.

---

## Основные признаки
- `Type` — тип продукта (`L`, `M`, `H`).
- `Air temperature [K]` — температура воздуха.
- `Process temperature [K]` — температура процесса.
- `Rotational speed [rpm]` — скорость вращения.
- `Torque [Nm]` — крутящий момент.
- `Tool wear [min]` — износ инструмента.

---

## Этапы работы
1. Загрузка данных.
2. Предобработка данных.
3. Разделение выборки на обучающую и тестовую.
4. Обучение моделей.
5. Оценка качества.
6. Визуализация результатов.

---

## Предобработка данных
- Удалены столбцы `UDI`, `Product ID`, `TWF`, `HDF`, `PWF`, `OSF`, `RNF`.
- Категориальный признак `Type` закодирован численно.
- Числовые признаки масштабированы с помощью `StandardScaler`.
- Пропущенные значения проверены перед обучением моделей.

---

## Модели
- Logistic Regression
- Random Forest
- XGBoost
- SVM

---

## Метрики оценки
- `Accuracy` — доля правильных предсказаний.
- `Confusion Matrix` — распределение правильных и ошибочных предсказаний.
- `Classification Report` — precision, recall и F1-score.
- `ROC-AUC` — качество разделения классов по вероятностям.

---

## Сравнение моделей
| Модель | Accuracy | ROC-AUC |
|---|---:|---:|
| Logistic Regression | 0.9735 | 0.8927 |
| Random Forest | 0.9840 | 0.9537 |
| XGBoost | 0.9850 | 0.9738 |
| SVM | 0.9695 | 0.8830 |

---

## Лучшая модель
- По совокупности метрик наилучший результат показала модель `XGBoost`.
- Она получила максимальные значения `Accuracy` и `ROC-AUC`.
- Поэтому именно такой подход особенно полезен для задачи раннего выявления отказов.

---

## Streamlit-приложение
- Страница «Анализ и модель»:
  загрузка CSV, обучение моделей, таблица метрик, ROC-кривые, confusion matrix, classification report, форма предсказания.
- Страница «Презентация»:
  краткое описание проекта, данных, этапов работы и полученных результатов.

---

## Практический результат
- Пользователь может загрузить датасет через интерфейс.
- После обучения доступны сравнительные метрики всех моделей.
- Можно выбрать модель и получить прогноз по новым значениям признаков.

---

## Заключение
- Реализовано приложение для бинарной классификации отказов оборудования.
- Выполнены загрузка данных, предобработка, обучение, сравнение моделей и предсказание.
- Полученные результаты можно использовать для оценки вероятности отказа оборудования.
- Дальнейшее развитие: настройка гиперпараметров и балансировка классов.
"""

    with st.sidebar:
        st.header("Настройки презентации")
        theme = st.selectbox(
            "Тема",
            ["black", "white", "league", "beige", "sky", "night", "serif", "simple", "solarized"],
            index=0,
        )
        height = st.number_input("Высота слайдов", value=500, step=10)
        transition = st.selectbox("Переход", ["slide", "convex", "concave", "zoom", "none"])
        plugins = st.multiselect(
            "Плагины",
            ["highlight", "katex", "mathjax2", "mathjax3", "notes", "search", "zoom"],
            [],
        )

    rs.slides(
        presentation_markdown,
        height=height,
        theme=theme,
        config={
            "transition": transition,
            "plugins": plugins,
        },
        markdown_props={"data-separator-vertical": "^--$"},
    )


presentation_page()
